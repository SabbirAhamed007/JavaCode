package com.company;

public class StringReference4324 {

    public static void main(String[] args){

        String txt = "Hello World !!!";

        txt.toLowerCase();

        System.out.println(txt);

        System.out.println(txt.endsWith("!!!"));
        System.out.println(txt.startsWith("j"));
        System.out.println(txt.length());
        System.out.println(txt.indexOf("W"));
        System.out.println(txt.replace("World", "Sabbir"));
        System.out.println(txt);
    }
}
