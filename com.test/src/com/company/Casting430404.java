package com.company;

public class Casting430404 {

    public static void main(String[] args){

        short x = 2;
        int y = x + 4;
        System.out.println(y);

        double p = 45.67;
        double k = p * 5;
        System.out.println(k);

        double o = 5.6;
        int i = (int)o + 4;
        System.out.println(i);

        String h = "5";

        int u = Integer.parseInt(h) + 5;

        System.out.println(h);
        System.out.println(u);

        String gg = "5.6";

        double pp = Double.parseDouble(gg) + 56.566454;
        System.out.println(pp);






    }
}
