package com.company;

public class ExpressionTest1 {


    public static void main(String[] args){


        // Expression = operands & Operator
        // Operands = values, variable , numbers, quantity
        // Oprator = * + - %


        int friends = 10;

        friends++;

        System.out.println(friends);

        friends = friends + 10;

        System.out.println(friends);

        friends = friends * 2;

        System.out.println(friends);

        friends = friends % 3;

        System.out.println(friends);


        double stu = 10;

        stu = stu / 3;
        System.out.println(stu);


    }
}
