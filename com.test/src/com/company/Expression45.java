package com.company;

public class Expression45 {

    public static void main(String[] args){


        int friends = 10;

        friends = friends + 5;

        System.out.println(friends);

        friends += 5;

        System.out.println(friends);

        friends++;

        System.out.println(friends);

        ++friends;

        System.out.println(friends);


    }
}
