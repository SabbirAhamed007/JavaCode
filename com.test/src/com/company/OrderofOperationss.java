package com.company;

public class OrderofOperationss {

    public static void main(String[] args){

        int x = 10 + 3 * 3;
        System.out.println(x);

        int y = (10 + 3) * 3;
        System.out.println(y);

        int z = 10 + 2 * (10 - 1);
        System.out.println(z);


    }
}
